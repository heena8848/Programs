#---->FOR LOOP
fruits=["apple","banana","cherry"]
for x in fruits:
    print(x)    #print each items 
else:
    print("----------")


for x in "cocoa":
    print(x)    #print each letter of value
else:
    print("----------")


for x in range(5):
    print(x)
else:
    print("----------")


for x in range(2,6):
    print(x)
else:
    print("----------")


for x in range(2,30,3):
    print(x)
